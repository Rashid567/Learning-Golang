package main

import (
	"fmt"
	"slices"
)

// –ù–µ –ø–æ–¥—Ö–æ–¥–∏—Ç –¥–ª—è —Å–æ—Å—Ç–∞–≤–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤, —Ç–∞–∫ –∫–∞–∫ –Ω–∞—Ä—É—à–∞–µ—Ç—Å—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∞. –ö –ø—Ä–∏–º–µ—Ä—É: 1Ô∏è‚É£2Ô∏è‚É£3Ô∏è‚É£
func ReverseString(value string) string {
	runes := []rune(value)
	slices.Reverse(runes)
	return string(runes)
}

func main() {

	strings2Reverse := []string{
		"–≥–ª–∞–≤—Ä—ã–±–∞",
		"üëçüëãüòä",
		"1Ô∏è‚É£2Ô∏è‚É£3Ô∏è‚É£",
	}

	for _, str := range strings2Reverse {
		reversedStr := ReverseString(str)
		fmt.Println("Original:", str)
		fmt.Println("Reversed:", reversedStr)
		fmt.Println()
	}

}
